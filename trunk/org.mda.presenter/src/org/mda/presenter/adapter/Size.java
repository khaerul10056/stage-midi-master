package org.mda.presenter.adapter;

public class Size {
	
	private final int width; 
	
	private final int height;
	
	public Size (final int width, final int height) {
		this.height = height;
		this.width = width;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

	

}
